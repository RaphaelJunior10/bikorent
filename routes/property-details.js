// Fonction pour r√©cup√©rer une propri√©t√© par son ID
async function getPropertyById(propertyId) {
    try {
        if (!firestoreUtils.isInitialized()) {
            console.log('Firebase non initialis√©, utilisation des donn√©es de test');
            return getMockPropertyById(propertyId);
        }

        // R√©cup√©rer toutes les propri√©t√©s et filtrer par ID
        const allProperties = await firestoreUtils.getAll('properties');
        console.log(`üìä Propri√©t√©s r√©cup√©r√©es depuis Firebase: ${allProperties.length}`);

        // Chercher la propri√©t√© avec l'ID correspondant
        const property = allProperties.find(p => p.id === propertyId || p.id.toString() === propertyId);
        
        // Debug: Afficher les d√©tails de la propri√©t√© trouv√©e
        if (property) {
            console.log('üîç D√©tails de la propri√©t√© trouv√©e:', {
                id: property.id,
                name: property.name,
                tenantId: property.tenantId,
                status: property.status,
                isOccupied: property.isOccupied,
                occupied: property.occupied,
                available: property.available,
                allFields: Object.keys(property)
            });
        }

        if (!property) {
            return null;
        }

        // Transformer les donn√©es Firebase en format compatible avec la vue
        const formattedProperty = formatPropertyData(property);
        console.log('üìã Propri√©t√© format√©e:', {
            id: formattedProperty.id,
            title: formattedProperty.title,
            status: formattedProperty.status,
            price: formattedProperty.price
        });

        return formattedProperty;

    } catch (error) {
        console.error('Erreur lors de la r√©cup√©ration de la propri√©t√©:', error);
        return getMockPropertyById(propertyId);
    }
}

// Fonction pour formater les donn√©es d'une propri√©t√©
function formatPropertyData(property) {
    // D√©terminer le statut bas√© sur les donn√©es disponibles
    let status = 'vacant';
    
    // V√©rifier plusieurs champs possibles pour d√©terminer si la propri√©t√© est lou√©e
    if (property.tenantId && property.tenantId !== '' && property.tenantId !== null) {
        status = 'rented';
    } else if (property.status && (property.status === 'rented' || property.status === 'lou√©' || property.status === 'occupied')) {
        status = 'rented';
    } else if (property.isOccupied === true || property.occupied === true) {
        status = 'rented';
    } else if (property.available === false) {
        status = 'rented';
    }
    
    console.log('üè† D√©termination du statut:', {
        id: property.id,
        tenantId: property.tenantId,
        status: property.status,
        isOccupied: property.isOccupied,
        occupied: property.occupied,
        available: property.available,
        finalStatus: status
    });

    // D√©terminer le type de propri√©t√©
    let type = 'appartement';
    if (property.type) {
        type = property.type.toLowerCase();
    } else if (property.name && property.name.toLowerCase().includes('studio')) {
        type = 'studio';
    } else if (property.name && property.name.toLowerCase().includes('maison')) {
        type = 'maison';
    }

    // Extraire le prix du loyer
    let price = 'N/A';
    if (property.rent) {
        price = `${property.rent} xaf/mois`;
    } else if (property.monthlyRent) {
        price = `${property.monthlyRent} xaf/mois`;
    }

    // Extraire la surface
    let area = 'N/A';
    if (property.area) {
        area = `${property.area}m¬≤`;
    } else if (property.surface) {
        area = `${property.surface}m¬≤`;
    }

    // Extraire le nombre de pi√®ces
    let rooms = 1;
    if (property.rooms) {
        rooms = property.rooms;
    } else if (property.bedrooms) {
        rooms = property.bedrooms + 1; // +1 pour le salon
    }

    // Extraire l'√©tage
    let floor = 'N/A';
    if (property.floor) {
        floor = property.floor;
    }

    // Cr√©er une description bas√©e sur les donn√©es disponibles
    let description = property.description || '';
    if (!description) {
        description = `Bien immobilier situ√© √† ${property.address || 'adresse non sp√©cifi√©e'}.`;
        if (property.features && property.features.length > 0) {
            description += ` √âquipements: ${property.features.join(', ')}.`;
        }
    }

    // G√©rer les images multiples
    let images = [];
    if (property.images && Array.isArray(property.images) && property.images.length > 0) {
        images = property.images;
    } else if (property.image) {
        images = [property.image];
    } else {
        images = [getDefaultImage(type)];
    }

    return {
        id: property.id,
        title: property.name || `Propri√©t√© ${property.id}`,
        location: property.address || 'Adresse non sp√©cifi√©e',
        city: property.city || 'Ville non sp√©cifi√©e',
        price: price,
        type: type,
        status: status,
        image: images[0], // Image principale pour la compatibilit√©
        images: images, // Tableau d'images pour le slider
        features: {
            rooms: rooms,
            area: area,
            floor: floor
        },
        description: description,
        // Donn√©es suppl√©mentaires pour les filtres
        rent: property.rent || property.monthlyRent || 0,
        tenantId: property.tenantId || null
    };
}

